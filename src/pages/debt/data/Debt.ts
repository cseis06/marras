// Función para obtener clientes por ranking
export const getTopPayers = (clients: ClientDebt[], limit: number = 5) => {
  return [...clients]
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map((c) => ({
      id: c.id,
      firstName: c.firstName,
      lastName: c.lastName,
      code: c.code,
      score: c.score,
      totalHistoric: c.totalHistoric,
    }));
};

export const getTopDefaulters = (clients: ClientDebt[], limit: number = 5) => {
  return [...clients]
    .filter((c) => c.status === 'overdue' || c.status === 'in_collection')
    .sort((a, b) => b.currentDebt - a.currentDebt)
    .slice(0, limit)
    .map((c) => ({
      id: c.id,
      firstName: c.firstName,
      lastName: c.lastName,
      code: c.code,
      score: c.score,
      pendingDebt: c.currentDebt,
      daysOverdue: c.daysOverdue,
      severity: (c.daysOverdue <= 30 ? 'low' : c.daysOverdue <= 60 ? 'moderate' : 'critical') as DebtSeverity,
    }));
};